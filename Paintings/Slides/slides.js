function next_slide() {

  if (current_slide + 1 === total_slides) {
    return;
  }
  current_slide = (current_slide + 1) % total_slides;

  render_slide();
}

function prev_slide() {
  if (current_slide === 0) {
    return;
  }
  current_slide = (current_slide - 1 + total_slides) % total_slides;

  render_slide();
}


